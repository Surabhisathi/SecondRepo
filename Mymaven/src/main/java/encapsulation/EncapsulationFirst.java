package encapsulation;

public class EncapsulationFirst {
	private String name;
	private int mark;
	public void setName(String name)
	{
		this.name=name;
	}
	public String getName()
	{
		return name;
		
	}
	public void setMark(int mark)
	{
		this.mark=mark;
	}
	public int getMark()
	{
		return mark;
		
	}
}
